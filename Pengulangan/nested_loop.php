<?php
// Dari 1 - 10
// For Baris
for ($i=1; $i <= 10 ; $i++) { 
    // For Bintang
    for ($a=1; $a <= $i ; $a++) { 
        echo " *";
    } echo "<br>";
}
echo "<br>";
// For 10 - 1
// For Baris
for ($i=10; $i >= 1 ; $i--) { 
    // For Bintang
    for ($a=1; $a <= $i ; $a++) { 
        echo " *";
    } echo "<br>";
}